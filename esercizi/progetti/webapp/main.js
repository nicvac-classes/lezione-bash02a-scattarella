// Applicazione principale
function saluta(nome) {
    console.log("Ciao, " + nome + "!");
}

function somma(a, b) {
    return a + b;
}

function sottrazione(a, b) {
    return a - b;
}

// Esecuzione
saluta("Studente");
console.log("2 + 3 = " + somma(2, 3));
console.log("10 - 4 = " + sottrazione(10, 4));
